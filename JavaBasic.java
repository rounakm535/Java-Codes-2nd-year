public class JavaBasic {

    public static void Printing() {
        System.out.println("***");
        System.out.println("**");
        System.out.println("*");

    }
    public static void main(String args[]) {
        int a = 100;
        int b = 300;

        //swap
        int temp  = a ;
        a = b;
        b = temp;

        System.out.println("A = " + a);
        System.out.println("B = " + b);
        
    }
}